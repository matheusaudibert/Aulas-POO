//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// Exemplo 08: ponteiros para funções
//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

#include <iostream>

using namespace std;


//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// Protótipo das funções
//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

int soma(int a, int b);
int subtrai(int a, int b);

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// Função Principal
//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
int main() {

    cout << "\n";
    cout << "Ponteiros para Funções\n\n";

    //Declara um ponteiro para função
    int(*operação)(int, int);

    //Atribui a função soma() ao ponteiro
    operacao = soma;

    //Chamando a função soma() através do ponteiro
    cout << "Soma: " << operacao(10, 5) << endl;

    //Atribuindo a função subtrai() ao ponteiro
    operacao = subtrai;

    //Chamando a função subtrai() através do ponteiro
    cout << "Subtração: " << operacao(10, 5) << endl;

    cout << "\n";

    return 0;

}

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// Definição das Funções
//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

//Função que realiza uma soma
int som(int a, int b) {
    return a + b;
}

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

//Função que realiza uma subtração
int subtrai(int a, int b) {
    return a - b;
}
